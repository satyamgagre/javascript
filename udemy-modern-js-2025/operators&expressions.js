// ARITHMETIC OPERATORS
let a = 10;
let b = 5;

console.log(a + b); // 15 ADDITION
console.log(a - b); // 5 SUBTRACTION
console.log(a * b); // 50 MULTIPLICATION
console.log(a / b); // 2 DIVISION
console.log(a % b); // 0 MODULUS OR REMINDER


// COMPARISON OPERATORS
let x = 10;
let y = 20;

console.log(x == y); // FALSE 10 IS NOT EQUAL TO 20
console.log(x != y); // TRUE 10 IS NOT EQUAL TO 20
console.log(x > y); // FALSE 10 IS NOT GREATER THAN 20
console.log(x < y); // TRUE 10 IS LESS THAN 20
console.log(x >= 10); // TRUE 10 IS GREATER THAN OR EQUAL TO 10
console.log(x <= 20); // TRUE 10 IS LESS THAN OR EQUAL TO 20


// LOGICAL OPERATORS
let isadult = true;
let HasID = false;

console.log(isadult && HasID); //FALSE BOTH NUST BE TRUE
console.log(isadult || HasID); //TRUE AT LEAST ONE IS TRUE
console.log(!isadult); // FALSE NEGATION OF TRUE 